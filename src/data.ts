// Returns suggestions matching category or some fallback news articles
export function getRecommendationsFor(articleId: string, limit: number = 3): Article[] {
  const active = NEWS_ARTICLES.find(a => a.id === articleId) || NEWS_ARTICLES[0];
  // Prioritize same category first, excluding the currently active article
  const sameCategory = NEWS_ARTICLES.filter(a => a.category === active.category && a.id !== active.id);
  // Fallback to other categories if not enough
  const others = NEWS_ARTICLES.filter(a => a.id !== active.id && a.category !== active.category);
  
  return [...sameCategory, ...others].slice(0, limit);
}
